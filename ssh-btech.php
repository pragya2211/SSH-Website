<h2>B.Tech Computer Science and Social Sciences (CSSS)</h2>
<br>
<p align="justify">This unique B.Tech in Computer Science (CS) and Social Sciences (SS), aims to develop CS knowledge and expertise in students, with a strong input from relevant social science disciplines. As a 4 year program, the B.Tech (CSSS) will satisfy the requirements of almost all higher studies programs in India as well as overseas. It may be an ideal program for those students who are not sure if they want to pursue engineering careers and would like to explore the possibility of going for social sciences later but want to be ready to take an CS career if desired.</p>
