<h1>Sociology and Social Anthropology</h1>
The curriculum for PhD programs in Sociology and Social Anthropology is
designed to strengthen fundamentals in theory and research
methods through Core/Compulsory courses and provide sufficient scope to
cultivate critical and innovative thinking through Independent Studies and
Seminar style courses.  The aim is to impart training to conduct high quality
research. 
<br><br>
The available areas of specialisation are:
Information Communication Technologies; Privacy and Surveillance;  Gig
Economy;  Smart Ageing;  Technology and Society;  Education and
Technology;
Urban Sociology; Urban Politics; Infrastructure Studies; Environment;
Waste and Pollution; Urban E-waste practices; Ecology; Anthropocene;
Precarious  
Caste; Class; Labour; Gender; Childhood; Inequality; Civility
Migration and Transnationalism; Demographic Anthropology; Family,
Kinship and Marriage
Students with fellowships are encouraged to apply. However, the institute
also offers limited stipends of Rs.31,000 per month in the first two years
and  Rs.35,000 per month in the third &amp; fourth year of their PhD.  Research
Scholars can be provided On-campus accommodation and personal work-
stations. Provision to support research paper/poster presentations in national
and international conferences is also available.

